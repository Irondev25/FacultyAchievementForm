package models;

/**
 *
 * @author bhask
 */
public class Mobile {
    int fid;
    String mobileNum;

    public Mobile(int fid, String mobileNum) {
        this.fid = fid;
        this.mobileNum = mobileNum;
    }

    public Mobile() {
    }

    public int getFid() {
        return fid;
    }

    public void setFid(int fid) {
        this.fid = fid;
    }

    public String getMobileNum() {
        return mobileNum;
    }

    public void setMobileNum(String mobileNum) {
        this.mobileNum = mobileNum;
    }
}
